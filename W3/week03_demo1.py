x=0b11111111    #將二進位整數11111111指派給變數x 
y=0o77          #將八進位整數77指派給變數y
z=0x4e          #將十六進位整數4e指派給變數z
print("整數---------------------------------")
print(x)        #印出變數x
print(y)        #印出變數y
print(z)        #印出變數z
print(type(x))
print(type(y))
print(type(z))
print("內建函數 int(參數)--------------------")
print(int('56')) #把參數轉換成整數資料型態
print("布林值--------------------------------")
bool1=True
bool2=False
print(bool1)
print(bool2)
print(type(bool1))
print(type(bool2))
print("內建函數 bool(參數)--------------------")
print(bool(1))
print(bool(0))
print(bool(None))
print(bool(''))
print("浮點數--------------------------------")
num1=3.14159
total= 9/2  #將9/2的結果指派給變數total
print(num1)
print(total)    #印出total的值
print(type(total))  #印出total的資料型態
print("內建函數 float(參數)--------------------")
total2=float('3')
total3=float(3)
print(total2)
print(total3)



